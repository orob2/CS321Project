    void outputVictory(int victoryValue) {
       if (victoryValue == 0)
           resultBox.setText("You tied!");
       else if (victoryValue == 1)
           resultBox.setText("You win!");
       else if (victoryValue == -1)
           resultBox.setText("You lose! Get your revenge!");
    }

